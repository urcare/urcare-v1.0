interface RiskFlag {
  id: string;
  type: 'medical' | 'fall' | 'infection' | 'medication' | 'deterioration' | 'behavioral';
  severity: 'low' | 'medium' | 'high' | 'critical';
  description: string;
  triggers: string[];
  lastUpdated: string;
  autoGenerated: boolean;
}

